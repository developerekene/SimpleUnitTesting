package com.example.testlearning

class Calculator {
    fun addingNumbers(a: Int, b:Int): Int{

        return a + b
    }

    fun subtractingNumbers(a: Int, b:Int): Int{

        return a - b
    }

    fun multiplicationNumbers(a: Int, b:Int): Int{

        return a * b
    }

    fun divisionNumbers(a: Int, b:Int): Int{

        return a / b
    }
}